import java.util.*;
public class ClearExample{
	public static void main(String[] args){
		ArrayList<String> n = new ArrayList<>();
		n.add("kiran");
		n.add("sai");
		n.add("kalyan");
		n.add("kumar");
		System.out.println(" "+n);
		n.clear();
		System.out.println(" "+n);
		
	}
}


